package control_statement;

//Loop
//1-10
//for(initialization, condition, increment/decrement)
public class ForLoopExample {
    public static void main(String[] args) {
        System.out.println("Numbers from 1-10");
        int i;
        for (i = 0; i <= 10; i++) {
            System.out.println(i);
        }
    }
}

